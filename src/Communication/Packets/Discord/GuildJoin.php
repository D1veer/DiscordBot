namespace JaxkDev\DiscordBot\Communication\Packets\Discord;

use JaxkDev\DiscordBot\Communication\BinaryStream;
use JaxkDev\DiscordBot\Communication\Packets\Packet;
use JaxkDev\DiscordBot\Models\Guild\Guild;

class GuildJoin extends Packet{

    public const SERIALIZE_ID = 12;

    private Guild $guild;

    public function __construct(Guild $guild, ?int $uid = null){
        parent::__construct($uid);
        $this->guild = $guild;
    }

    public function getGuild(): Guild{
        return $this->guild;
    }

    public function binarySerialize(): BinaryStream{
        $stream = new BinaryStream();
        $stream->putInt($this->getUID());
        $stream->putSerializable($this->guild);
        return $stream;
    }

    public static function fromBinary(BinaryStream $stream): self{
        $uid = $stream->getInt();
        return new self(
            $stream->getSerializable(Guild::class), // guild
            $uid
        );
    }
}